<script>
    $(function(){
        $.ajaxSetup({
            headers:{
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });

        var table = $('.data-table').DataTable({
            processing: true,
            serverSide: true,
            ajax: "{{ route('product.index') }}",
            columns : [
                {data:'DT_RowIndex',name:'DT_RowIndex'},
                {data:'name',name:'name'},
                {data:'price',name:'price'},
                {data:'details',name:'details'},
                // {data:'image',name:'image'},
                // { data: 'image', name: 'image', sortable: false, searchable: false },
                {data: 'action', name: 'action', orderable: false, searchable: false},
            ]
        });

        $('#createNewProduct').click(function () {
            $('#saveBtn').val("create-product");
            $('#product_id').val('');
            $('#productForm').trigger("reset");
            $('#modelHeading').html("Create New Product");
            $('#ajaxModel').modal('show');
        });

        $('body').on('click', '.editProduct', function () {
            var product_id = $(this).data('id');
            $.get("{{ route('product.index') }}" +'/' + product_id +'/edit', function (data) {
            $('#modelHeading').html("Edit Product");
            $('#saveBtn').val("edit-user");
            $('#ajaxModel').modal('show');
            $('#product_id').val(data.id);
            $('#name').val(data.name);
            $('#price').val(data.price);
            $('#details').val(data.details);
            })
        });


        $('#saveBtn').click(function (e) {
            e.preventDefault();
            $(this).html('Sending..');
            // var datav=$('#productForm').serialize();
           
            // alert(datav);
        
            $.ajax({
                data: $('#productForm').serialize(),
                url: "{{ url('product.store')}}",
                type: "POST",
                cache: false,
                contentType: false,
                processData: false,
                success: function (data) {
                    if(data.errors)
                        {
                            printErrorMsg(data.errors); 
                            // location.reload();
                            alert(data.errors);
                          
                        }
                        else{
                            $('#productForm').trigger("reset");
                            $(".print-error-msg").css('display','none');
                            $('#ajaxModel').modal('hide');
                            table.draw();
                            alert("done");

                        }
                   
                },
                error: function (data) {
                    console.log('Error:', data);
                    $('#saveBtn').html('Save Changes');
                    alert(2);
                }

            });
        });




      
        function printErrorMsg (msg) {
        $(".print-error-msg").find("ul").html('');
        $(".print-error-msg").css('display','block');
        $.each( msg, function( key, value ) {
            $(".print-error-msg").find("ul").append('<li>'+value+'</li>');
        });
    }

        $('body').on('click', '.deleteProduct', function (){
            var product_id = $(this).data("id");
            var result = confirm("Are You sure want to delete !");
            if(result){
                $.ajax({
                    type: "DELETE",
                    url: "{{ route('product.store') }}"+'/'+product_id,
                    success: function (data) {
                        table.draw();


                    },
                    error: function (data) {
                        console.log('Error:', data);
                    }
                });
            }else{
                return false;
            }
        });
    });


    $(document).ready(function() {
       $('#exportButton').click(function() {
           fetch('/export', { //This line uses the fetch API to make a GET request to the "/export" URL on your server. It starts the process of requesting data from the server.                    method: 'GET',
                    headers: {                       
                        'Content-Type': 'application/json',                  
                      },
               })
                .then((response) => response.blob())
                .then((blob) => {
                    // Create a blob URL and trigger download
                    const url = window.URL.createObjectURL(blob);
                    const a = document.createElement('a');
                    a.href = url;
                    a.download = 'product.xlsx';
                  document.body.appendChild(a);
                   a.click();
                   window.URL.revokeObjectURL(url);
               })
                .catch((error) => {
                   console.error('Export failed:', error);
                   alert('Export failed.');
                });

        });
   });

